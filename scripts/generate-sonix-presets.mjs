// Generate src/generated/sonixPresets.ts from a directory of Sonix .instr files.
//
// Parses each Synthesis/MIDI .instr through the REAL Sonix C decode (tools/sonix-audit/
// gen-presets.c) so the params exactly match what the WASM produces, then emits an
// InstrumentPreset config array merged into FACTORY_PRESETS.
//
// Usage: node scripts/generate-sonix-presets.mjs [instrumentsDir]
//   default instrumentsDir: /Users/spot/Downloads/sonix/Instruments
import { execFileSync } from 'node:child_process';
import { mkdtempSync, writeFileSync } from 'node:fs';
import { tmpdir } from 'node:os';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const REPO = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const INCLUDE = path.join(REPO, 'sonix-wasm/src');
const GEN_C = path.join(REPO, 'tools/sonix-audit/gen-presets.c');
const OUT = path.join(REPO, 'src/generated/sonixPresets.ts');
const dir = process.argv[2] || '/Users/spot/Downloads/sonix/Instruments';

const bin = path.join(mkdtempSync(path.join(tmpdir(), 'sonix-gen-')), 'gen');
execFileSync('cc', ['-O1', '-w', '-I', INCLUDE, GEN_C, '-o', bin, '-lm'], { stdio: 'inherit' });
const json = execFileSync(bin, [dir], { encoding: 'utf-8', maxBuffer: 64 * 1024 * 1024 });
const raw = JSON.parse(json);

// De-dupe by name (keep first), sort by name.
const seen = new Set();
const presets = raw
  .filter((p) => (seen.has(p.name) ? false : (seen.add(p.name), true)))
  .sort((a, b) => a.name.localeCompare(b.name))
  .map((p) => ({
    type: 'synth',
    name: p.name,
    synthType: 'SonixSynth',
    volume: 0,
    pan: 0,
    effects: [],
    parameters: {
      sonixIndex: 0,
      sonix: {
        index: 0,
        baseVol: p.baseVol, portFlag: p.portFlag, c2: p.c2, c4: p.c4,
        filterBase: p.filterBase, filterRange: p.filterRange, filterEnvSens: p.filterEnvSens,
        envScanRate: p.envScanRate, envLoopMode: p.envLoopMode, envDelayInit: p.envDelayInit,
        envVolScale: p.envVolScale, envPitchScale: p.envPitchScale, slideRate: p.slideRate,
        wave: p.wave, envTable: p.envTable, lfoWave: p.lfoWave,
        egLevels: p.egLevels, egRates: p.egRates,
      },
    },
  }));

const header = `/**
 * sonixPresets — AUTO-GENERATED by scripts/generate-sonix-presets.mjs. DO NOT EDIT.
 *
 * Sonix synth instruments ripped from .instr files, parsed through the real Sonix WASM
 * decode and merged into FACTORY_PRESETS so they appear in the SonixSynth editor's preset
 * list. SonixSynth re-synthesises from these params (WASM render-one-note audition).
 *
 * ${presets.length} presets.
 * Regenerate: node scripts/generate-sonix-presets.mjs [instrumentsDir]
 */
import type { InstrumentPreset } from '@typedefs/instrument';

export const SONIX_PRESETS: InstrumentPreset['config'][] = ${JSON.stringify(presets, null, 2)} as InstrumentPreset['config'][];
`;

writeFileSync(OUT, header);
console.log(`wrote ${OUT} with ${presets.length} presets`);
