#!/usr/bin/env node
/**
 * Generate src/engine/registry/effects/buzzmachine.ts from BUZZMACHINE_INFO.
 *
 * Reads the effect-type entries (and their indexed params) out of
 * src/engine/buzzmachines/BuzzmachineEngine.ts and emits an EffectDescriptor per machine.
 * The audio path reuses BuzzmachineSynth; live param updates dispatch through its setParam.
 *
 * Run: node scripts/gen-buzzmachine-effects.cjs
 * After running, add any NEW ids to the AudioEffectType union (src/types/instrument/effects.ts)
 * and the lazy registration in src/engine/registry/effects/index.ts.
 */
const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const ENGINE = path.join(ROOT, 'src/engine/buzzmachines/BuzzmachineEngine.ts');
const OUT = path.join(ROOT, 'src/engine/registry/effects/buzzmachine.ts');

const eng = fs.readFileSync(ENGINE, 'utf8');

// enum key -> string value (BuzzmachineType = { KEY: 'Value', ... } as const)
const map = {};
const enumBlock = eng.slice(eng.indexOf('BuzzmachineType = {'), eng.indexOf('} as const'));
let m;
const reEnum = /(\w+):\s*['"]([A-Za-z0-9]+)['"]/g;
while ((m = reEnum.exec(enumBlock))) map[m[1]] = m[2];

// Walk BUZZMACHINE_INFO entries, brace-matched, keep type:'effect'
const infoStart = eng.indexOf('BUZZMACHINE_INFO');
const reEntry = /\[BuzzmachineType\.(\w+)\]:\s*\{/g;
reEntry.lastIndex = eng.indexOf('{', infoStart);
const effects = [];
while ((m = reEntry.exec(eng))) {
  const key = m[1];
  let j = m.index + m[0].length - 1, d = 0;
  for (; j < eng.length; j++) { if (eng[j] === '{') d++; else if (eng[j] === '}') { d--; if (d === 0) { j++; break; } } }
  const block = eng.slice(m.index, j);
  const type = (block.match(/type:\s*['"](\w+)['"]/) || [])[1];
  if (type !== 'effect') continue;
  const name = (block.match(/name:\s*['"]([^'"]+)['"]/) || [])[1];
  const params = [];
  const reP = /index:\s*(\d+),[\s\S]*?name:\s*['"]([^'"]+)['"][\s\S]*?minValue:\s*([0-9xa-fA-F]+),[\s\S]*?maxValue:\s*([0-9xa-fA-F]+),[\s\S]*?defaultValue:\s*([0-9xa-fA-F]+)/g;
  let p;
  while ((p = reP.exec(block))) params.push({ i: +p[1], n: p[2], mn: Number(p[3]), mx: Number(p[4]), df: Number(p[5]) });
  effects.push({ key, name, params });
}

const group = (n) => {
  const s = n.toLowerCase();
  if (/dist|overdrive|sat|exciter|crush/.test(s)) return 'Distortion';
  if (/filter|svf|notch|zfilter|philta|wah/.test(s)) return 'Filter';
  if (/delay|verb|reverb|echo/.test(s)) return 'Reverb & Delay';
  if (/chorus|phase|flang|trem|vibrato/.test(s)) return 'Modulation';
  if (/shift|pitch/.test(s)) return 'Pitch';
  if (/comp|limit|gate|gain/.test(s)) return 'Dynamics';
  return 'Filter';
};

let out = `/**
 * Buzzmachine effect descriptors — GENERATED by scripts/gen-buzzmachine-effects.cjs from
 * BUZZMACHINE_INFO (effect-type machines). Reuses the proven BuzzmachineSynth audio path
 * (input -> WASM worklet -> output); create() seeds the indexed params and live updates
 * dispatch through BuzzmachineSynth.setParam. Do not hand-edit — regenerate.
 */
import { EffectRegistry } from '../EffectRegistry';
import type { EffectDescriptor } from '../EffectDescriptor';
import type { EffectConfig } from '@typedefs/instrument';
import { BuzzmachineType } from '@engine/buzzmachines/BuzzmachineEngine';
import { BuzzmachineSynth } from '@engine/buzzmachines/BuzzmachineSynth';

async function makeBuzz(type: string, config: EffectConfig): Promise<BuzzmachineSynth> {
  const s = new BuzzmachineSynth(type as never);
  await s.ensureInitialized();
  for (const [k, v] of Object.entries(config.parameters || {})) {
    const idx = Number(k);
    if (Number.isInteger(idx)) s.setParameter(idx, Number(v));
  }
  return s;
}

const BUZZ_EFFECTS: EffectDescriptor[] = [
`;
for (const e of effects) {
  const val = map[e.key];
  if (!val) { console.error('no enum value for', e.key); continue; }
  const defs = e.params.map((p) => `'${p.i}': ${p.df}`).join(', ');
  const pdefs = e.params.map((p) => {
    const t = (p.mn === 0 && p.mx === 1) ? 'toggle' : 'knob';
    return `{ key: '${p.i}', label: ${JSON.stringify(p.n)}, type: '${t}', min: ${p.mn}, max: ${p.mx}, default: ${p.df} }`;
  }).join(',\n      ');
  const p0 = e.params[0];
  const lvl = (f) => Math.round(p0.mn + (p0.mx - p0.mn) * f);
  const presets = `[
      { name: 'Gentle', params: { '${p0.i}': ${lvl(0.15)} } },
      { name: 'Medium', params: { '${p0.i}': ${lvl(0.5)} } },
      { name: 'Strong', params: { '${p0.i}': ${lvl(0.85)} } },
    ]`;
  out += `  {
    id: '${val}', name: ${JSON.stringify(e.name)}, category: 'wasm', group: '${group(e.name)}',
    loadMode: 'lazy',
    create: (c: EffectConfig) => makeBuzz(BuzzmachineType.${e.key}, c),
    getDefaultParameters: () => ({ ${defs} }),
    parameters: [
      ${pdefs},
    ],
    presets: ${presets},
  },
`;
}
out += `];

EffectRegistry.register(BUZZ_EFFECTS);

export const BUZZ_EFFECT_IDS = BUZZ_EFFECTS.map((e) => e.id);
`;
fs.writeFileSync(OUT, out);
console.log(`Wrote ${OUT} with ${effects.length} buzz effects.`);
console.log('IDs:', effects.map((e) => map[e.key]).join(' '));
